package edu.emory.cci.aiw.cvrg.eureka.common.json;

import org.codehaus.jackson.annotate.JsonCreator;
import org.codehaus.jackson.annotate.JsonIgnore;
import org.codehaus.jackson.annotate.JsonProperty;

public abstract class PrimitiveParameterMixin {
    @JsonCreator
    public PrimitiveParameterMixin(@JsonProperty("id") String id) { }
    
    @JsonIgnore public abstract boolean isConcatenable();
    @JsonIgnore public abstract boolean isSolid();
    @JsonIgnore public abstract String[] getChildren();
}
